package model;

public class OwnerVO {
    private int o_no; // owner 일련번호
    private int m_no; // member 일련번호
    private int p_no; // player 일련번호

    public OwnerVO() {
    }

    public int getO_no() {
        return o_no;
    }

    public void setO_no(int o_no) {
        this.o_no = o_no;
    }

    public int getM_no() {
        return m_no;
    }

    public void setM_no(int m_no) {
        this.m_no = m_no;
    }

    public int getP_no() {
        return p_no;
    }

    public void setP_no(int p_no) {
        this.p_no = p_no;
    }

}